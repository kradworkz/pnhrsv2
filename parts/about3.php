                <h6>This year’s conference: </h6>
                <div class="font-size-11" style="text-align: justify;">
                <p>The Zamboanga Consortium for Health Research and Development (ZCHRD) in collaboration with the Department of Science and Technology (DOST) IX, will host the 2021 PNHRS Week Celebration while we are still in the state of national health emergency due to the CoViD-19 Pandemic streaming live. The PNHRS Week will be a two – week celebration to highlight all the activities/trainings of the different consortia in the country. Zamboanga City attempted on several occasions to host the PNHRS week. All these attempts were foiled by many unforeseen predicaments, the CoViD-19 pandemic among others.</p>

                <p>This year’s theme, ““Health R and D in the New Normal: Moving Towards Universal Health Care”, is an opportunity for all the researchers/speakers/participants to share their best practices and experiences in pursuit of healthier lives for the Filipinos through health research and development. The theme highlights the importance of health research as we navigate through the pandemic with a defined course of the new normal towards achieving Universal Health Care (UHC). Embedded in it are research projects that aspire to bring the goal of UHC to fruition as we engage relevant sectors/stakeholders on latest information, initiate expert discussions on recent and pressing health data/problems, and stimulate dynamic exchange of ideas, and sharing of best practices and experiences. </p>

                
