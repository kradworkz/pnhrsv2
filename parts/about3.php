                <h6>Logo & Description: </h6>
                <div class="font-size-11" style="text-align: justify;">
                    <p>The 2021 PHILIPPINE NATIONAL HEALTH RESEARCH SYSTEM (PNHRS) Week Celebration logo incorporates elements that showcase the diverse and united people of this year’s host – Zamboanga City.</p>
                    <p>The Vinta and its multicolored sail takes diversity front and center; an everyday means of transport, the paddleboat ferries people of various ethnicities, along with needed health and medical supplies to and from the city’s neighboring islands, whilst navigating through uneven tides and unpredictable waters.</p>
                    <p>Alluding to the encompassing reach of Universal Health Care, the umbrella represents the PNHRS’ inclusivity as an organization that champions health and health-related research via its multi-pronged programs accomplished by the various 17 REGIONAL consortia nation-wide.</p>
                    <p>The ring symbolizes the unity and harmony of the PNHRS in working towards holistic health care for the Filipino people. It encircles the entire logo to highlight the significance of health research in achieving universal health care in the Philippines.</p>
